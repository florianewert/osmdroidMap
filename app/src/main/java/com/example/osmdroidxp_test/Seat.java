package com.example.osmdroidxp_test;

public class Seat {

    String i, b1, b2, b3, x, y, s, p, k, r;

    public Seat(String i, String b1, String b2, String b3, String x, String y, String s, String p, String k, String r) {
        this.i = i;
        this.b1 = b1;
        this.b2 = b2;
        this.b3 = b3;
        this.x = x;
        this.y = y;
        this.s = s;
        this.p = p;
        this.k = k;
        this.r = r;
    }

    public String getI() {
        return i;
    }

    public String getB1() {
        return b1;
    }

    public String getB2() {
        return b2;
    }

    public String getB3() {
        return b3;
    }

    public String getX() {
        return x;
    }

    public String getY() {
        return y;
    }

    public String getS() {
        return s;
    }

    public String getP() {
        return p;
    }

    public String getK() {
        return k;
    }

    public String getR() {
        return r;
    }
}
